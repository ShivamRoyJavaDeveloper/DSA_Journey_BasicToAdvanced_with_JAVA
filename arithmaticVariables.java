public class arithmaticVariables {
    public static void main(String[]args){
        int a;
        int b;
        a = 12;
        b = 10;
        float c = (a/b);
        System.out.println(a*b);
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b); // 1.2
        System.out.println(c);

        // if int replaced by float it will show decimal value with accurate answer
        float d,f;
        d=12;
        f=10;
        System.out.println(d/f);
    }
}
